using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace For_Loop
{
    internal class Number_45to25
    { // Number between 45 to 25
        static void Main(string[] args)
        {
            Console.WriteLine("Number between 45 to 25 :");
            for(int i=45;i>=25;i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
