using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace For_Loop
{
    internal class Number_21to30
    {
        // display number between 21 to30.
        static void Main(string[] args)
        {
            Console.WriteLine("Number Between 21 to 30");

            for(int i=21;i<=30;i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
